<div class="common-padd">
      <div class="col-90 diseases-main">
          <h2 class="head-title">Cirrhosis & Portal hypertension</h2>
          <div class="row">
              <div class="col-md-12">
                  <p class="my-para">When the liver gets damaged uniformly by toxins, bile, drugs, alcohol, hepatitis infections or by certain genetic conditions, the normal tissue gets replaced with scars. As the disease progresses the scars coalesce to form bridging fibrosis. Liver cirrhosis progressively shrinks the liver. As the volume of normal functioning liver cells reduce, liver failure ensues. Advanced liver failure is incompatible with life and will need a liver transplant.</p>
                  <h5 class="para-head">Complications of Cirrhosis</h5>
                  <ul class="para-ul">
                    <li>Edema and ascites - When the liver loses its ability to make the protein albumin, water accumulates in the leg (edema) and abdomen (ascites).</li>
                    <li>Bruising and bleeding - proteins needed for blood clotting stop getting produced leading to bruising and bleeding.</li>
                    <li>Jaundice - yellowing of the skin and eyes that occurs when the diseased liver does not absorb enough bilirubin.</li>
                    <li>Itching - Bile products deposited in the skin may cause intense itching.</li>
                    <li>Gallstones - Cirrhosis prevents bile from reaching the gallbladder leading to formation of gallstones.</li>
                    <li>Toxins in the blood or brain - a damaged liver cannot remove toxins from the blood, causing them to accumulate in the blood and brain, leading to reduced mental functioning and can cause personality changes, coma, and even death. Signs of the buildup of toxins (like ammonia) in the brain include neglect of personal appearance, unresponsiveness, forgetfulness, trouble concentrating, or changes in sleep habits.</li>
                    <li>Portal hypertension - Blood from the intestines and spleen is carried to the liver through the portal vein. Cirrhosis slows the normal flow of blood through the portal vein, which increases the pressure inside it. This condition is called portal hypertension. Portal vein carries blood with nutrients from the intestines to the liver, for further processing. The pressure gradient is required to push the blood across the liver tissues, into the heart. However when the liver shrinks from disease, the resistance to blood flow increases several fold. This state is called portal hypertension. This results in accumulation of fluid in the abdomen (ascites) and diversion of blood flow through some narrow bypass vessels, which results in varices. Variceal veins are thin walled and found lining the upper stomach and lower food pipe. When the pressure increases beyond a threshold, the wall can break, resulting in severe bleeding. Hence, ascites, GI bleed, hypersplenism and portal hypertensive gastropathy are the result of portal hypertension.</li>
                    <li>Varices - When blood flow through the portal vein faces resistance in the liver, blood from the intestines and spleen backs up into blood vessels in the stomach and esophagus. These blood vessels may become enlarged,called varices. These veins have thin walls and carry blood under high pressure, and thus are more likely to burst and bleed.</li>
                    <li>Problems in other organs - Cirrhosis can cause immune system dysfunction, leading to infection. Ascites (fluid) in the abdomen may become infected (SBP) with bacteria normally present in the intestines, and cirrhosis can also lead to kidney dysfunction and failure.</li>
                  </ul>
                  <h5 class="para-head">Diagnosis</h5>
                  <ul class="para-ul">
                    <li>Symptoms and signs as explained above</li>
                    <li>Physical examination – liver feels harder or larger than usual, may be nodular or lumpy.</li>
                    <li>Laboratory tests – low albumin and abnormal prothrombin time are vital indicators</li>
                    <li>Ultrasound/ CT scan may show shrunken liver</li>
                    <li>Liver biopsy in early stages will confirm cirrhosis</li>
                  </ul>
                  <h5 class="para-head">Treatment</h5>
                  <p class="my-para">Cirrhosis caused by alcohol abuse is treated by abstaining from alcohol. Treatment for hepatitis-related cirrhosis involves medications used to treat the different types of hepatitis.  Cirrhosis caused by Wilson's disease, in which copper builds up in organs, is treated with medications to remove the copper. These are just a few examples of treatment for cirrhosis.<br><br>
                  Ascites and edema can be treated with a low-sodium diet, fluid restriction or the use of diuretics. Antibiotics will be prescribed for infections, and various medications can help with itching. Protein causes toxins to form in the digestive tract, so eating less protein will help decrease the buildup of toxins in the blood and brain. Laxatives help prevent absorption of toxins generated by ingested proteins from the intestines.<br><br>
                  For portal hypertension, beta-blocker is prescribed. Variceal bleeds can be injected with a clotting agent or perform a rubber-band ligation, Glue injection and elastic banding (EVL), and oesophageal ballooning or TIPSS for severe bleeds.  The enlarged spleen hyperfunctions, causing a fall in all the cell types in the blood like white and red cells as well as platelets.<br><br>
                  When complications cannot be controlled or when the liver becomes so damaged from scarring that it stops functioning well, a liver transplant is necessary. MELD score is used as an index to determine whether a patient needs a liver transplant.  A score greater than 15 is a clear indication to advise transplant. When this MELD level is breached the patient’s median survival is less than 18 months. In liver transplantation surgery, a diseased liver is removed and replaced with a healthy one from an organ donor. About 80 to 90 percent of people survive liver transplantation.
                  </p>
