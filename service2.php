                  <h2 class="head-title">KIDNEY TRANSPLANTION</h2>
                  <h5 class="para-head">Need for a Kidney Transplantation</h5>
                  <p class="my-para">All patients with chronic kidney failure in the age group of 2 to 75 years can benefit from kidney transplantation. Common causes of kidney failure needing transplant include diabetes, glomerular diseases, interstitial fibrosis, reflux nephropathy, hypertension etc. Outcomes – in cadaver kidney transplant the 5 year graft survival is 70%, and 10 year survival is 60%.  In living related transplants the 5 year graft survival is 80% and 10 years is 70%.  The first year graft survival is 90% for both types of transplants.</p>
                  <h5 class="para-head">Types of kidney transplants offered</h5>
                  <ul>
                    <li>Cadaver donor kidney transplant</li>
                    <li>Living-donor kidney transplant</li>
                    <li>Living donor swap kidney transplant</li>
                    <li>Combined liver/pancreas & kidney transplant</li>
                  </ul>

                  <h5 class="para-head">Transplant recipient criteria</h5>
                  <span class="service-span">A patient has to fulfil the following criteria:</span>
                  <ul>
                    <li>No history of active cancer</li>
                    <li>No ongoing infection</li>
                    <li>No history of significant or untreated cardiovascular disease such as heart failure, heart attack, stroke, poor blood circulation in the legs</li>
                    <li>No history of not following up with treatments and appointments</li>
                    <li>No history of drug abuse and mental diseases</li>
                    <li>Patients will have to undergo a series of medical tests and specialist screening to ensure suitability for transplant.</li>
                    <li>Not have positive crossmatch test, high panel reactive antibody titre, positive donor specific antibodies (these are routinely screened in all patients)</li>
                  </ul>

                  <h5 class="para-head">Living kidney donor criteria</h5>
                  <span class="service-span">A living kidney donor must be a healthy individual who fulfils the following criteria:</span>
                  <ul>
                    <li>At least 21 years of age up to 65 years</li>
                    <li>Have an established relationship with the potential recipient. For example, parent, sibling, spouse or grandparent (first degree relative)</li>
                    <li>Able to remain healthy with one kidney</li>

                    <li>Have no higher risk of developing kidney failure than a person who has 2 functioning kidneys</li>
                    <li>Have no conditions that can be potentially transmitted to the potential recipient. For example, cancer or infection</li>
                    <li>No history of drug abuse</li>
                    <li>No active mental disorder or intellectual impairment that might impair their ability to comprehend the consequences of kidney donation.</li>
                    <li>Morbid obesity, COPD and other chronic debilitating health status</li>
                  </ul>

                  <h2 class="head-title">Living donation of kidney</h2>
                  <p class="my-para">Any relative with a compatible blood group and normal function of all major body systems can safely donate one of their kidneys. Dr. M.R. Rajasekar has pioneered kidney removal by laparoscopy in India and has the largest experience in the country. Laparoscopic donor nephrectomy enables donors to get back to normal state within 2 to 3 days and have a much smaller scar.  Pain and other morbidities are reduced significantly.  The outcome of laparoscopic kidney removal is same as that for robotic surgery.  After complete medical and psychological evaluation the papers supporting relationship are submitted to the transplant approval committee. Clearance is usually obtained in few weeks, after which a suitable date is fixed for the transplant.</p>

                   <h2 class="head-title">Cadaver donor transplantation</h2>
                  <p class="my-para">Patients who do not have related donors are evaluated and kept on a waitlist for a cadaver organ. Patients need to undergo complete evaluation and provide a list of documents required by TRANSTAN to complete the process of listing. Few days visit to the ‘GLOBAL ORGAN TRANSPLANT CENTER’ and few dialysis is mandatory. The waiting time is variable and depends on the rate of donation and the number of patients waiting for that blood group. Outcome of cadaver organ transplant is comparable to that of live donor kidney transplant.</p>

