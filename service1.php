                  <h2 class="head-title">LIVER TRANSPLANTION</h2>
                  <h5 class="para-head">Need for a liver transplant</h5>
                  <p class="my-para">Patients with advanced acute liver failure, progressive liver disease or liver cancer, will benefit from liver transplant. A Model for End stage Liver Disease Score (MELD score) greater than 12 indicates the need to evaluate and recommend liver transplantation. Clinical incidents like, GI bleed, peritonitis, recurrent fluid collection in the belly or attacks of liver coma are indications for early liver transplantation. Absolute indications for liver transplant</p>
                  <ul>
                    <li>Ascites (fluid in the belly): Recurrent collection, not responding to diuretic medicines or repeated removal by needle</li>
                    <li>GI bleed: Vomiting blood or passing blood in stool, more than once</li>
                    <li>Sub-acute Bacterial Peritonitis (SBP): Spontaneous infection in the belly fluid (Polymorph cell count >250/cc)</li>
                    <li>Coma: Repeated admission to hospital for loss of orientation and conscious state</li>
                    <li>Serum Albumin consistently below 3.5 Gm/dl</li>
                    <li>Prothrombin time 5 seconds above the control lab value (or INR>1.5)</li>
                    <li>Serum total bilurubin >6 mg/dl, especially in patients with bile outflow problems like- PSC & PBC</li>
                  </ul>
                  <p class="my-para">The outcomes depend on the cause, reporting time, early correct medical therapy and provision of good intensive care. The only absolute contraindications being HIV infection and wide spread cancer (primary or secondary). Transplant is also not advisable in patients with heart and lung failure, those with systemic infections (TB), severe malnutrition and cancer without liver cirrhosis.</p>

                  <h2 class="head-title">Types of Liver Transplant</h2>
                  <h5 class="para-head">Living Donor Liver Transplant (LDLT)</h5>
                  <p class="my-para">A live person donates part of his/her healthy liver to replace the recipient's diseased liver.  For a patient requiring a liver transplant, first any potential living donors who have volunteered to donate part of his/her liver, is assessed. The main advantage of living-donor liver transplant (LDLT) is that there is no waiting time, other than the time needed to work up the donor and to get approval from the Government instituted Transplant Committee.  (Donating part of the liver hyperlink pop up)</p>
                  <h5 class="para-head">Deceased Donor Liver Transplant (DDLT)</h5>
                  <p class="my-para">To receive a liver from a deceased donor (cadaver organ transplant), patient’s name and other details are entered onto the waiting list after completion of evaluation by the Dr. Rajasekar’s specialized team.  The coordinator will provide the list of documents that are required to complete the listing process.  While on the waiting list, patient’s underlying liver disease will be managed by the team to ensure an optimal condition until a donor liver become available. Each time a donor liver becomes available, the best matched patient, in terms of blood group, body weight and also in terms of urgency, is called in for transplant. Waiting time is generally few weeks to several months depending on the availability of the right blood group and the length of the waiting list. <br>The LDLT is more complex than DDLT, though the long term outcomes are comparable.</p>
                  <h5 class="para-head">Split liver transplant</h5>
                  <p class="my-para">In situ split is a complex procedure where a healthy donor liver is split into two portions like live donor surgery and the two divided parts are given to two different patients.  This can be two children or an adult and a child.  (Give GG Transplant page link)</p>
                  <h5 class="para-head">Paediatric liver transplant</h5>
                  <p class="my-para">Children of all age groups with liver failure can avail of a lifesaving transplant.  A small part of the liver is usually donated by one of the parents. OR the child can benefit from a split liver graft.</p>

                  <h2 class="head-title">The liver transplant process</h2>
                  <p class="my-para">The entire diseased liver is surgically removed and a healthy liver or part of the liver is surgically implanted in its place. This requires an operative wound called "Inverted T" in the upper abdomen.<br>
                  The operation is complex and takes six to twelve hours. Removing the old liver can be slow, especially if you have had previous surgery. The blood vessels and bile duct of the new liver need to be carefully reconnected and this can be time-consuming.<br>
                  In the living-donor liver transplant, part of the liver from the healthy living donor is removed and attached into the recipient, with both operations taking place simultaneously. </p>

                  <h2 class="head-title">Living donor liver surgery</h2>
                  <p class="my-para">The donor is selected from close relatives based on blood group match, mental and physical status, thorough medical evaluation and an extensive process of counselling.  Donor safety is given the highest priority in our program.<br>The surgery on the living donor takes about 5 hours and about 50% of the liver is carefully removed from the donor for implantation.  Majority of them will not require even blood transfusion. The average hospital stay for donors is around 7 to 10 days.  The full liver re-growth is achieved within 3 months.</p>

                  <h2 class="head-title">Recovering after a liver transplant</h2>
                  <p class="my-para">The new liver usually starts functioning immediately.  You will be removed from the ventilator assistance in the first 24 to 48 hours.  You will be kept in a dedicated intensive care facility with expert nurses and doctors monitoring your progress closely.  You will be made to eat and walk within the first 3 days. In order for the liver transplant to be successful, various medications like immunosuppressants, antibiotics and other supportive medicines are given... You can expect to be in the hospital for 3 to 4 weeks and once found fit will be discharged for outpatient follow up.  Liver transplant patients need to be closely followed-up by their liver specialist and may also need other medicines for other pre-existing and/or transplant related conditions.  </p>

                  <h2 class="head-title">Follow up after transplantation</h2>
                  <p class="my-para">Detailed check-up will be performed by all the specialists involved before you are released from the hospital.  Your blood will be sampled at least thrice a week to monitor the transplanted organ. Blood level of immunosuppressive medicines will be monitored. Ultrasound and CT scan may be performed.  Whenever the blood tests are abnormal a liver biopsy will be done under local anaesthesia. Repeat admissions may be required to treat infections, rejection or other surgical complications. Out station patients may be required to stay for 4 to 6 weeks after discharge to fine tune the treatment.<br>(The following para can be a pop up in LDLT paragraph)<br>Donating part of the liver<br>Partial liver donation by relatives of the patient is very common the world over.  The liver is a single organ and can be split anatomically into two useful portions safely. Also, the liver has in excess of 300% reserve capacity and can theoretically survive with only 30% of the total liver volume. The liver is also very unique in its ability to regenerate. It is documented that the liver regenerates to 90% of the original liver volume within 3 months of removing > 50% of the liver for donation. Liver donation is a major surgery and will leave a large scar. In Dr. Rajasekar’s series, there are a few minor complications with 60% of donors not needing even a blood transfusion. The only notable complication has been a bile leak from the raw area of the liver that settled spontaneously within 3 weeks. Most donors were discharged within 10 days of surgery and they can return to non-manual work 4-6 weeks after surgery and full active life within 3 months. The only long term morbidity seems to be wound pain which may persist beyond 3 months in about 30% of the donors. Occasional death has been reported after live donation which underlines the fact that the procedure should be carried out only by experts in this field in transplant centers with good track record, who can guarantee donor safety and low complication rate.</p>

