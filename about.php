                      <p>After training in Transplantation in the United Kingdom and the University of Chicago Hospitals in the US, wanting to bring this highly skilled lifesaving technique to India, he set up the first Multi organ Transplant programme in New Delhi in 1996. Establishing a state of the art Transplant ICU and Unit, he performed the first successful Liver transplant on Sanjay Kandaswamy, a baby from his hometown Kanchipuram, which was also the first Pediatric and first living-related liver transplant in the country (1998). Sanjay is now 17 years old and has joined MBBS to pursue the field that gave him a new lease of life. The same year he also successfully performed the first cadaver liver transplant in the country. He has to his credit around 640 liver transplants, and has innovated novel surgical techniques in live donor transplantation and to treat complex liver tumors.</p>
